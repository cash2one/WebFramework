<h3>testNG是一个测试框架，来源于JUnit和NUnit，不过它也提供了如下的一些更强大以及更容易使用的功能:</h3>
<pre>
 1. <a target="_blank" href="http://testng.org/doc/documentation-main.html#annotations">注解</a>
 2. 以多种方式在线程池中运行你的测试（多线程运行）， 比如测试方法上的单线程执行，或者在测试类上的单线程等
 3. 测试你的代码是否是多线程安全的
 4. 灵活的测试配置
 5. 支持数据驱动的测试(@DataProvider)
 6. 支持测试方法函数中有参数
 7. 强大的执行模式(no more TestSuite)
 8. 相关的工具和插件(Eclipse, IDEA, Maven等)
 9. 支持BeanShell, 以增加灵活性
10.默认JDK中的实时性和日志功能
11.用于应用程序服务测试的独立的方法
</pre>
